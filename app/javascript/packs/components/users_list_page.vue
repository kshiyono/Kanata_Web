<template>
  <body>
    <div id="app">
      <table>
        <tbody>
          <tr>
            <th>ID</th>
            <th>name</th>
            <th>email</th>
          </tr>
          <tr v-for="user in users" :key="user.id">
            <td>{{ user.id }}</td>
            <td>{{ user.name }}</td>
            <td>{{ user.email }}</td>
          </tr>
        </tbody>
      </table>
